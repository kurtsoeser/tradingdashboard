export type KnownTickerSuggestion = {
  ticker?: string;
};

export function normalizeBasiswertKey(raw: string): string {
  return raw
    .trim()
    .toLowerCase()
    .replace(/\./g, "")
    .replace(/-/g, " ")
    .replace(/\s+/g, " ");
}

